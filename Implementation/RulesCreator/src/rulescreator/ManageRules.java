    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here: write to rules file
        try
        {
            String[] s = new String[jList1.getModel().getSize()];
            int i = 0;
            while (i < jList1.getModel().getSize())
            {
                s[i] = jList1.getModel().getElementAt(i).toString();
                i++;
            }
            manage.writeToFile(s);
        }
        catch (IOException e)
        {
        
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: add new rule
        Window win = SwingUtilities.getWindowAncestor(this);
        JDialog dlg = new JDialog(win, "Rules Dialog",Dialog.ModalityType.APPLICATION_MODAL);
        RulesModel rules = new RulesModel("");
        dlg.getContentPane().add(rules);
        dlg.pack();
        dlg.setLocationRelativeTo(null);
        dlg.setVisible(true);
        dlg.setResizable(false);
        //listModel.addElement(rules.ruleBuilder);
        eRules.add(rules.ruleBuilder);
        listModel = new DefaultListModel();
        int i = 0;
            while (i < eRules.size())
            {
                listModel.addElement(eRules.get(i));  
                i++;
            }
        jList1.setModel(listModel);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:  edit selected rule
        Window win = SwingUtilities.getWindowAncestor(this);
        JDialog dlg = new JDialog(win, "Rules Dialog",Dialog.ModalityType.APPLICATION_MODAL);
        RulesModel rules = new RulesModel(jList1.getSelectedValue().toString());
        int index = eRules.indexOf(jList1.getSelectedValue().toString());
        dlg.getContentPane().add(rules);
        dlg.pack();
        dlg.setLocationRelativeTo(null);
        dlg.setVisible(true);
        dlg.setResizable(false);
        //listModel.addElement(rules.ruleBuilder);
        eRules.set(index , rules.ruleBuilder);
        listModel = new DefaultListModel();
        int i = 0;
            while (i < eRules.size())
            {
                listModel.addElement(eRules.get(i));  
                i++;
            }
        jList1.setModel(listModel);
    }//GEN-LAST:event_jButton2ActionPerformed

