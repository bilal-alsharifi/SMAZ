    private DefaultListModel fillList()
    {//read rules from file
        try
        {
            
            String [] s = manage.readFromFile();            
            int i = 0;
            while (s[i] != null)
            {
                listModel.addElement(s[i]);  
                eRules.add(s[i]);
                i++;
            }
        }
        catch (IOException e)
        {
        
        }
        return listModel;
    }
